async function getApplicablePageLayouts(allLayouts: Map<string, LayoutInformation>, pagePathname: string): Promise<LayoutInformation[]> {
    const pageLayouts: LayoutInformation[] = [];

    for (const [_, layoutInformation] of allLayouts.entries()) {
        const doesLayoutApply = pagePathname.startsWith(layoutInformation.pathname);

        if (!doesLayoutApply) continue;

        pageLayouts.push(layoutInformation);
    }

    pageLayouts.sort((a: LayoutInformation, b: LayoutInformation) => a.pathname.length - b.pathname.length);

    return pageLayouts;
}

async function gatherAllPages(allLayouts: Map<string, LayoutInformation>): Promise<Map<string, PageInformation>> {
    const pageMap = new Map();

    await walkDirectory(compilerOptions.pagesDirectory, async (file) => {
        if (file.name !== "page.ts") return;

        const fullPath = path.join(file.parentPath, file.name);
        const pathname = sanitizePathname(path.relative(compilerOptions.pagesDirectory, file.parentPath));

        const exports = await getPageExports(fullPath);
        const applicablePageLayouts = await getApplicablePageLayouts(allLayouts, pathname);

        const pageInformation: PageInformation = {
            modulePath: fullPath,
            exports: exports,
            pathname: pathname,
            applicableLayouts: applicablePageLayouts,
        };

        pageMap.set(pathname, pageInformation);
    })

    return pageMap;
}

async function gatherAllLayouts(): Promise<Map<string, LayoutInformation>> {
    const layoutMap = new Map();

    await walkDirectory(compilerOptions.pagesDirectory, async (file) => {
        if (file.name !== "layout.ts") return;

        const fullPath = path.join(file.parentPath, file.name);
        const pathname = sanitizePathname(path.relative(compilerOptions.pagesDirectory, file.parentPath));

        const exports = await getLayoutExports(fullPath);

        const layoutInformation: LayoutInformation = {
            modulePath: fullPath,
            exports: exports,
            pathname: pathname,
        };

        layoutMap.set(pathname, layoutInformation);
    })

    return layoutMap;
}

const compiledStaticLayouts = new Map<string, CompiledLayout>();
async function getCompiledLayout(layoutInformation: LayoutInformation, allLayouts: Map<string, LayoutInformation>): Promise<CompiledLayout> {
    if (layoutInformation.exports.isDynamic === true) {
        return await compileLayout(layoutInformation);
    }

    if (compiledStaticLayouts.has(layoutInformation.pathname)) {
        return compiledStaticLayouts.get(layoutInformation.pathname)!;
    }

    const compiledLayout = await compileLayout(layoutInformation);
    compiledStaticLayouts.set(layoutInformation.pathname, compiledLayout);

    return compiledLayout;
}

async function compilePageToDisk(
    allLayouts: Map<string, LayoutInformation>, 
    pageInformation: PageInformation
): Promise<CompiledPage> {
    const compiledPage = await compilePage(allLayouts, pageInformation);

    const targetPath = path.join(
        getDistDir(),
        pageInformation.pathname,
        "index.html",
    );

    writeFileSync(targetPath, compiledPage.pageHTML)

    return compiledPage;
}

/** Returns the standard children of <head> that must exist on every page independent of it's content. */
function getEnforcedMetadata(): string {
    return `<script defer="true" src="/client.js"></script>`;
}
async function compilePage(
    allLayouts: Map<string, LayoutInformation>, 
    pageInformation: PageInformation
): Promise<CompiledPage> {
    const compilationContext = generatePageCompilationContext(pageInformation.pathname);

    const exports = pageInformation.exports;
    const pageConstructor = exports.pageConstructor;
    const pageMetadataConstructor = exports.pageMetadataConstructor;

    /**
     * Populate compilerStore.
     * Usage of AsyncLocalStorage allows us to have "globals" without globals,
     * as each call to run has it's own "context", removing concurrency issues.
     */
    const serverSubjects: ServerSubject<any>[] = [];
    const eventListeners: EventListener<any>[] = [];
    const serverObservers: ServerObserver<any>[] = [];
    const loadHooks: LoadHook<any>[] = [];
    const storeTools: CompilerStore = {
        generateId: () => generateId(compilationContext),
        addServerSubject: (subject) => {
            serverSubjects.push(subject);
        },

        addEventListener: (eventListener) => {
            eventListeners.push(eventListener);
        },

        addServerObserver: (serverObserver) => {
            serverObservers.push(serverObserver)
        },

        addLoadHook(loadHook) {
            loadHooks.push(loadHook);
        },

        compilationContext,
    };

    let pageRootElement = await compilerStore.run(storeTools, async () => {
        return await pageConstructor();
    })

    let pageRootMetadataElement = await compilerStore.run(storeTools, async () => {
        return await pageMetadataConstructor();
    })

    let pageSerializationResult: SerializationResult;
    let pageMetadataSerializationResult: SerializationResult;
    try {
        pageSerializationResult = serializeElement(compilationContext, pageRootElement);
        pageMetadataSerializationResult = serializeElement(compilationContext, pageRootMetadataElement);
    } catch(e) {
        console.error(`Failed to serialize the elements of a page with the pathname: "${pageInformation.pathname}"`);
        throw e;
    }

    const allServerSubjects = [ ...serverSubjects, ];
    const allEventListeners = [ ...eventListeners, ];
    const allServerObservers = [ ...serverObservers ];
    const allLoadHooks = [ ...loadHooks ];

    const allSpecialElementOptions = [
        ...pageSerializationResult.specialElementOptions, 
        ...pageMetadataSerializationResult.specialElementOptions,
    ];

    const pageHTML = pageSerializationResult.serializedElement;
    const pageMetadataHTML = pageMetadataSerializationResult.serializedElement;

    let finalHTML: string = "<!DOCTYPE html>";

    // go through the layouts, and construct the end-stage page html
    if (pageInformation.applicableLayouts.length > 0) {
        const rootLayout = pageInformation.applicableLayouts[0];
        const compiledRootLayout = await getCompiledLayout(rootLayout, allLayouts);

        const htmlTagIndex = compiledRootLayout.layoutHTMLStart.indexOf("<html");
        const htmlTagEndIndex = compiledRootLayout.layoutHTMLStart.indexOf(">");

        if (htmlTagIndex === -1 || htmlTagEndIndex === -1) {
            throw invalidLayoutError(compilerOptions, rootLayout.modulePath, "The root layout must start with an html() element.");
        }

        if (compiledRootLayout.layoutHTMLStart.includes("<body") === false) {
            throw invalidLayoutError(compilerOptions, rootLayout.modulePath, "The root layout must contain the body() element.");
        }

        const beforeHead = compiledRootLayout.layoutHTMLStart.substring(0, htmlTagEndIndex + 1);
        const afterHead = compiledRootLayout.layoutHTMLStart.substring(htmlTagEndIndex + 1);

        // gather the content of the head element, and inject it right after the <html> opening tag
        let headContent = "";
        {
            headContent += "<head>";
                headContent += getEnforcedMetadata();

                for (const layoutInformation of pageInformation.applicableLayouts) {
                    const compiledLayout = await getCompiledLayout(layoutInformation, allLayouts);

                    headContent += compiledLayout.layoutMetadataHTML;

                    allSpecialElementOptions.push(...compiledLayout.specialElementOptions);
                    allServerSubjects.push(...compiledLayout.serverSubjects);
                    allEventListeners.push(...compiledLayout.eventListeners);
                }
                
                headContent += pageMetadataHTML

            headContent += "</head>";
        }

        finalHTML += beforeHead + headContent + afterHead;

        // skip the root layout, since we already added it in.
        for (let i = 1; i < pageInformation.applicableLayouts.length; i++) {
            const layoutInformation = pageInformation.applicableLayouts[i];

            const compiledLayout = await getCompiledLayout(layoutInformation, allLayouts);

            finalHTML += compiledLayout.layoutHTMLStart;
        }

        finalHTML += pageHTML

        let endString = "";

        for (const layoutInformation of [...pageInformation.applicableLayouts].reverse()) {
            const compiledLayout = await getCompiledLayout(layoutInformation, allLayouts);

            endString += compiledLayout.layoutHTMLEnd;
        }

        const htmlEndTagIndex = endString.indexOf("</body>");
        if (htmlEndTagIndex === -1) {
            throw internalCompilerError("Failed to find </body> tag whilst compiling a page");
        }

        const beforeEndTag = endString.substring(0, htmlEndTagIndex);
        const afterEndTag = endString.substring(htmlEndTagIndex);

        const pageDataScript = await generatePageDataScript(compilationContext, allSpecialElementOptions, {
            serverSubjects: allServerSubjects, 
            eventListeners: allEventListeners,
            serverObservers: allServerObservers,
            loadHooks: allLoadHooks,
        });

        finalHTML += beforeEndTag + pageDataScript + afterEndTag;
    } else {
        finalHTML += `<html lang="en-us">`;
            finalHTML += "<head>";
                finalHTML += getEnforcedMetadata();
                finalHTML += pageMetadataHTML;
            finalHTML += "</head>";
            
            finalHTML += "<body>";
                finalHTML += pageHTML;

                const pageDataScript = await generatePageDataScript(compilationContext, allSpecialElementOptions, {
                    serverSubjects: allServerSubjects, 
                    eventListeners: allEventListeners,
                    serverObservers: allServerObservers,
                    loadHooks: allLoadHooks,
                });

                finalHTML += pageDataScript;
            finalHTML += "</body>";
        finalHTML += `</html>`;
    }

    const compiledPage = {
        pageHTML: finalHTML,
    };

    return compiledPage;
}

async function compileStaticPages(
    allLayouts: Map<string, LayoutInformation>, 
    allPages: Map<string, PageInformation>
): Promise<Map<string, CompiledPage>> {
    const compiledPages = new Map();

    for (const [pagePathname, pageInformation] of allPages) {
        if (pageInformation.exports.isDynamic === true) continue;

        const compiledPage = await compilePage(allLayouts, pageInformation);
        
        compiledPages.set(pagePathname, compiledPage);
    }

    return compiledPages;
}

