/** Context of a page that is currently being compiled. */
type PageCompilationContext = {
    /** The slash starting relative pathname (relative to pagesDirectory) of this page. */
    pathname: string,

    /** 
     * An id counter starting from 0, that is incremented once per generateID() call, 
     * and is used in conjunction with the pathname to generate unique static IDs (order-dependent) 
     */
    idCounter: number,
    usedHashes: string[],

    /**
     * Used by some tools to determine whether or not the current context is a page or a layout,
     * useful for stuff like loadHooks, which behave differently in layouts and pages.
     */
    kind: "page" | "layout",
};

/** Context of a layout that is currently being compiled. */
type LayoutCompilationContext = {
    /** The slash starting relative pathname (relative to pagesDirectory) of this layout. */
    pathname: string,

    /** 
     * An id counter starting from 0, that is incremented once per generateID() call, 
     * and is used in conjunction with the pathname to generate unique static IDs (order-dependent) 
     */
    idCounter: number,
    usedHashes: string[],

    /**
     * Used by some tools to determine whether or not the current context is a page or a layout,
     * useful for stuff like loadHooks, which behave differently in layouts and pages.
     */
    kind: "layout" | "page",
};

type CompilerOptions = {
    pagesDirectory: string;

    environment: "production" | "development",

    publicDirectory: string;

    outputDirectory: string;

    /**
     * **NOTE: Be careful with this!**
     * page.ts and layout.ts are compiled in isolated v8 modules, for hot-reload and stability purposes.
     * However, there might be times where you have some kind of database instance you need to reference, etc. inside of your page or layout.
     * For this purpose, you can pass in any value and have it be defined in the *global scope* of that particular module.
     * 
     * You should be *very* careful that you do not *assign values* into the globalThis that is passed in to the module, since then, the module cannot be gc'd.
     * If the module cannot be gc'd, every time that it is hot-reloaded it will leak memory, which is obviously bad.
     * 
     * The same practice applies for the usage of process event listeners inside of pages and layouts, as well as uncleaned intervals and timeouts.
     * You *may* expose a cleanup() function within a page to get rid of any unintended side-effects.
     */
    extendedGlobals: any[],
};

type CompiledLayout = {
    /** Compiled result of the layoutConstructor */
    layoutHTMLStart: string,
    layoutHTMLEnd: string,

    /** Client data tokens generated by the layoutConstructor & layoutMetadataConstructor during it's compilation. */
    specialElementOptions: { elementKey: string, optionName: string, optionValue: SpecialElementOption }[],

    serverSubjects: ServerSubject<any>[],
    eventListeners: EventListener<any>[],
    loadHooks: LoadHook<any>[],

    /** Compiled result of the layoutMetadataConstructor */
    layoutMetadataHTML: string,
};

type CompiledPage = {
    pageHTML: string,
};

/** The result of turning an element into a string and extracting any special options from it. */
type SerializationResult = { 
    serializedElement: string, 
    specialElementOptions: { elementKey: string, optionName: string, optionValue: SpecialElementOption }[],
};

