/** Context of a page that is currently being compiled. */
type PageCompilationContext = {
    /** The slash starting relative pathname (relative to pagesDirectory) of this page. */
    pathname: string,

    /** 
     * An id counter starting from 0, that is incremented once per generateID() call, 
     * and is used in conjunction with the pathname to generate unique static IDs (order-dependent) 
     */
    idCounter: number,
    usedHashes: string[],
};

/** Context of a layout that is currently being compiled. */
type LayoutCompilationContext = {
    /** The slash starting relative pathname (relative to pagesDirectory) of this layout. */
    pathname: string,

    /** 
     * An id counter starting from 0, that is incremented once per generateID() call, 
     * and is used in conjunction with the pathname to generate unique static IDs (order-dependent) 
     */
    idCounter: number,
    usedHashes: string[],
};

type CompilerOptions = {
    pagesDirectory: string;

    outputDirectory: string;
};

type CompiledLayout = {
    /** Compiled result of the layoutConstructor */
    layoutHTML: string,

    /** Client data tokens generated by the layoutConstructor & layoutMetadataConstructor during it's compilation. */
    serializedClientDataTokens: string[],

    /** Compiled result of the layoutMetadataConstructor */
    layoutMetadataHTML: string,
};

/** The result of turning an element into a string and extracting any special options from it. */
type SerializationResult = { serializedElement: string, serializedClientDataTokens: string[] };

let compilerOptions: CompilerOptions;

function getDistDir() {
    const fullPath = path.join(compilerOptions.outputDirectory, "DIST");

    if (existsSync(fullPath) === false) {
        mkdirSync(fullPath, { recursive: true, })
    }

    return fullPath;
}

function setCompilerOptions(newOptions: CompilerOptions) {
    newOptions.pagesDirectory = path.resolve(newOptions.pagesDirectory);
    newOptions.outputDirectory = path.resolve(newOptions.outputDirectory);

    if (existsSync(newOptions.pagesDirectory) === false) {
        throw new Error(newOptions.pagesDirectory + " does not exist, and thus cannot be used as the pagesDirectory.");
    }

    if (existsSync(newOptions.outputDirectory) === false) {
        mkdirSync(newOptions.outputDirectory, { recursive: true, })
    }

    compilerOptions = newOptions;
}

/** 
 * Generate a stable, page-specific & order-dependent unique-identifier. 
 * NOTE: This function will throw if it fails to generate a hash after 100+ attempts.
 * However, this is not technically possible.
 */
function generateId(compilationContext: PageCompilationContext): string {
    let id: string;
    let tries = 0;

    while (true) {
        compilationContext.idCounter += 1;

        id = crypto
            .createHash('sha256')
            .update(compilationContext.pathname + ':' + compilationContext.idCounter.toString())
            .digest('base64url')
            .slice(0, 11); // 66 bits of entropy

        if (!compilationContext.usedHashes.includes(id)) {
            break;
        }

        tries += 1;

        if (tries > 100) {
            throw new Error("Failed to generate a unique id after 100+ attempts");
        }
    }
    
    compilationContext.usedHashes.push(id);

    return id;
}

function internalCompilerError(reason: string) {
    return new Error(`The compiler has encountered an internal error.\n${reason}`);
}

/** Take any directory pathname, and make it into this format: /path */
function sanitizePathname(pathname: string = ""): string {
    if (!pathname) return "/";

    pathname = "/" + pathname;

    pathname = pathname.replace(/\/+/g, "/");

    if (pathname.length > 1 && pathname.endsWith("/")) {
        pathname = pathname.slice(0, -1);
    }

    return pathname;
}

function getElementKey(compilationContext: PageCompilationContext, element: EleganceElement<any>): string {
    if (element.key) return element.key;

    element.key = generateId(compilationContext);

    return element.key;
}

function generatePageCompilationContext(pathname: string): PageCompilationContext {
    pathname = sanitizePathname(pathname);

    return {
        pathname: pathname,
        idCounter: 0,
        usedHashes: [],
    };
}

function generateLayoutCompilationContext(pathname: string): LayoutCompilationContext {
    pathname = sanitizePathname(pathname);

    return {
        pathname: pathname,
        idCounter: 0,
        usedHashes: [],
    };
}

function serializeEleganceElement(
    compilationContext: PageCompilationContext,
    element: EleganceElement<any>
): SerializationResult {
    let serializedElement = "";
    let serializedClientDataTokens: string[] = [];

    serializedElement += `<${element.tag}`;
    
    // Process options.
    {
        const options = Object.entries(element.options);

        if (options.length > 0) {
            for (const [optionName, optionValue] of options) {
                serializedElement += ` ${optionName}="${optionValue}"`;
            }
        }
    }

    serializedElement += ">";
    
    // Process children.
    {
        if (element.children === null) {

            return { serializedElement, serializedClientDataTokens, };
        }

        if (element.children.length > 0) {
            for (const child of element.children) {
                const result = serializeElement(compilationContext, child)

                serializedElement += result.serializedElement;
                serializedClientDataTokens.push(...result.serializedClientDataTokens);
            }
        }
    }

    serializedElement += `</${element.tag}>`;

    return { serializedElement, serializedClientDataTokens, };
}

function serializeElement(
    compilationContext: PageCompilationContext,
    element: AnyElement,
): SerializationResult {
    let serializedClientDataTokens: string[] = [];
    let serializedElement: string;

    if (element === undefined || element === null) {
        throw invalidElementError(element, "Undefined and null is not valid elements.");
    }

    switch (typeof element) {
    case "object":
        if (Array.isArray(element)) {
            serializedElement = element.join(", ");

            break;
        }

        if (element instanceof EleganceElement) {
            const entries = Object.entries(element.options);

            for (const [optionName, optionValue] of entries) {
                if (optionValue instanceof SpecialElementOption) {
                    optionValue.mutate(element, optionName);

                    const clientDataTokenString = optionValue.serialize(element, optionName);
                    serializedClientDataTokens.push(clientDataTokenString);
                }
            }

            const result = serializeEleganceElement(compilationContext, element);

            serializedElement = result.serializedElement;
            serializedClientDataTokens.push(...result.serializedClientDataTokens);

            break;
        }

        throw invalidElementError(element, "This element is an arbitrary object, and arbitrary objects are not valid children. Please make sure all elements are one of: EleganceElement, boolean, number, string or Array.")
    case "boolean":
        serializedElement = `${element}`;
        break;
    case "number":
        serializedElement = element.toString();
        break;
    case "string":
        serializedElement = element;
        break;
    default:
        throw invalidElementError(element, "The typeof of this element as not one of EleganceElement, boolean, number, string or Array. Please convert it into one of these types.");
    }

    return { serializedElement, serializedClientDataTokens, };
}

async function generatePageDataScript(compilationContext: PageCompilationContext, serializedClientDataTokens: string[]) {
    let scriptString = `export const data = [${serializedClientDataTokens.join(",")}];\n`;

    const transformedResult = await esbuild.transform(scriptString, { minify: true, });
    scriptString = transformedResult.code;
    
    let fullPath = path.join(
        getDistDir(), 
        compilationContext.pathname, 
        "page_data.js",
    );
    
    let dataDidChange = false;

    if (existsSync(fullPath)) {
        const existingContent = readFileSync(fullPath);

        if (existingContent.toString() !== scriptString) {
            dataDidChange = true;
        }
    }

    return { scriptString, fullPath, dataDidChange };
}

/** 
 * Go through a directory, including all it's subdirectories, 
 * and call callback() for each file.
 */
async function walkDirectory(fullPath: string, callback: (file: Dirent) => Promise<void>) {
    const stack: Dirent[] = [];

    stack.push(...readdirSync(fullPath, { withFileTypes: true, }));

    while (true) {
        const entry = stack.pop();
        if (!entry) break;

        if (entry.isDirectory()) {
            const fullPath = path.join(entry.parentPath, entry.name);

            stack.push(...readdirSync(fullPath, { withFileTypes: true, }));

            continue;
        }

        if (!entry.isFile()) continue;

        await callback(entry);
    }
}

/** 
 * Get a structured, validated and formatted version of the exports of a page.ts file
 * This file *should* be the first thing that imports a page.
 */
async function getPageExports(modulePath: string): Promise<PageExports> {
    const rawExports = await import("file://" + modulePath);

    let isDynamic = rawExports?.isDynamic === true

    const pageConstructor = rawExports.page;
    {
        if (typeof pageConstructor !== "function") {
            throw invalidPageError(compilerOptions, modulePath, "The type of the export \"page\" is not a function, and is instead of type: " + typeof pageConstructor);
        }
    }

    const pageMetadataConstructor = rawExports.metadata;
    {
        if (typeof pageMetadataConstructor !== "function") {
            throw invalidPageError(compilerOptions, modulePath, "The type of the export \"metadata\" is not a function, and is instead of type: " + typeof pageMetadataConstructor);
        }
    }
    
    return {
        isDynamic,
        pageConstructor,
        pageMetadataConstructor,
    };
}

